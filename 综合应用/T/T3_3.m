clc;clear;
%导入音频数据
[org_sound,fs_music]=audioread('../sound/plastic_love2.mp3');
len1=length(org_sound);t1=0:1/fs_music:(len1-1)/fs_music;
subplot(2,1,1);plot(t1,org_sound(:,1));xlabel('t/s');ylabel('幅度');
title('原始信号左声道时域波形');
org_fft1=fft(org_sound(:,1)',1024);
subplot(2,1,2);mstem(org_fft1);title('原始信号左声道信号频谱');
%采样率为32000时时域波形及频谱
figure(2);
fs1=32000;res_sound1=resample(org_sound,fs1,fs_music);
len2=length(res_sound1);t2=0:1/fs1:(len2-1)/fs1;
subplot(2,1,1);plot(t2,res_sound1(:,1));xlabel('t/s');ylabel('幅度');
title('减采样后左声道时域波形,fs=32000');
res_fft1=fft(res_sound1(:,1)',1024);
subplot(2,1,2);mstem(res_fft1);title('fs=32000重采样后左声道信号频谱');
%采样率为4000时时域波形及频谱
figure(3);
fs2=4000;res_sound2=resample(org_sound,fs2,fs_music);
len4=length(res_sound2);t4=0:1/fs2:(len4-1)/fs2;
subplot(2,1,1);plot(t4,res_sound2(:,1));xlabel('t/s');ylabel('幅度');
title('减采样后左声道时域波形,fs=4000');
res_fft2=fft(res_sound2(:,1)',1024);
subplot(2,1,2);mstem(res_fft2);title('fs=4000重采样后左声道信号频谱');
%对不同采样率下的音频信号进行播放
Org=audioplayer(org_sound,fs_music);play(Org);pause(3);
Fs1=audioplayer(res_sound1,fs1);play(Fs1);pause(3);
Fs2=audioplayer(res_sound2,fs2);play(Fs2);pause(3);